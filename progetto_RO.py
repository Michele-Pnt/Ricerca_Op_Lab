def solver(instance_data, time_limit_sec=180):
    """
    Risolve il problema Job Shop a partire da una lista di dizionari.

    Args:
        instance_data (list): Lista di dizionari {'job': str, 'op': str, 'machine': str, 'duration': int}
        time_limit_sec (int): Tempo limite in secondi per il solver.
    """

    # print("--- INIZIO ELABORAZIONE ---")

    # ==========================================
    # 1. PRE-PROCESSING DEI DATI
    # ==========================================
    # Convertiamo la lista piatta in una struttura organizzata per Job
    # jobs_data[job_index] = [(machine_id, duration), (machine_id, duration), ...]

    # Usiamo un dizionario temporaneo per raggruppare: job_id -> lista di operazioni
    temp_jobs = collections.defaultdict(list)
    unique_machines = set()

    for item in instance_data:
        # Assumiamo che i job partano da 1 nel dizionario, quindi sottraiamo 1 per averli 0-based
        j_id = int(item['job']) - 1
        op_id = int(item['op'])
        m_id = int(item['machine'])
        dur = int(item['duration'])

        #otteniamo le macchine uniche
        unique_machines.add(m_id)

        # Salviamo la tupla (operazione, macchina, durata)
        temp_jobs[j_id].append((op_id, m_id, dur))

    # Determiniamo il numero di job e macchine
    num_jobs = len(temp_jobs)
    num_machines = max(unique_machines) + 1 if unique_machines else 0

    # Costruiamo la lista finale jobs_data ordinata
    jobs_data = []
    # Ordiniamo le chiavi dei job per assicurarci di processare Job 1, poi Job 2, ecc.
    sorted_job_indices = sorted(temp_jobs.keys())

    for j in sorted_job_indices:
        # Ordina le operazioni del job j in base all'id operazione (op)
        # Questo garantisce che la sequenza tecnologica sia rispettata
        ops = sorted(temp_jobs[j], key=lambda x: x[0])

        # Estrae solo (macchina, durata) scartando l'indice op ormai inutile
        cleaned_ops = [(x[1], x[2]) for x in ops]
        jobs_data.append(cleaned_ops)

    # ==========================================
    # 2. CREAZIONE DEL MODELLO CP-SAT
    # ==========================================
    model = cp_model.CpModel()

    # Strutture dati per salvare le variabili e recuperarle dopo la risoluzione
    machine_to_intervals = collections.defaultdict(list)
    all_tasks = {} # struttura all_tasks : (job_id, task_id) -> (start_var, end_var)

    # Calcolo orizzonte temporale (Upper Bound del Makespan = somma di tutte le durate)
    horizon = sum(task[1] for job in jobs_data for task in job)

    # Variabile Obiettivo: Makespan
    makespan = model.NewIntVar(0, horizon, 'makespan')

    # 3. CREAZIONE VARIABILI E VINCOLI
    for job_id, job in enumerate(jobs_data):
        previous_end_time = None

        for task_id, task in enumerate(job):
            machine = task[0]
            duration = task[1]
            suffix = f'_{job_id}_{task_id}'

            # Variabili decisionali: inizio e fine del task
            start_var = model.NewIntVar(0, horizon, 'start' + suffix)
            end_var = model.NewIntVar(0, horizon, 'end' + suffix)

            # Variabile Intervallo (fondamentale per i vincoli di non sovrapposizione)
            interval_var = model.NewIntervalVar(start_var, duration, end_var, 'interval' + suffix)

            # Salviamo le variabili
            all_tasks[job_id, task_id] = (start_var, end_var)
            machine_to_intervals[machine].append(interval_var)

            # VINCOLO 1: Precedenza Tecnologica (Chain constraint)
            # Il task attuale deve iniziare dopo la fine del precedente nello stesso job
            if previous_end_time is not None:
                model.Add(start_var >= previous_end_time)

            previous_end_time = end_var

        # VINCOLO 2: Definizione Makespan
        # Il makespan deve essere >= alla fine dell'ultima operazione di questo job
        model.Add(makespan >= previous_end_time)

    # VINCOLO 3: Risorse Disgiuntive (No Overlap)
    # Su ogni macchina, gli intervalli non possono sovrapporsi
    for machine in range(num_machines):
        model.AddNoOverlap(machine_to_intervals[machine])

    # 4. OBIETTIVO
    model.Minimize(makespan)

    # ==========================================
    # 5. RISOLUZIONE
    # ==========================================
    solver = cp_model.CpSolver()
    solver.parameters.max_time_in_seconds = time_limit_sec

    # print("Avvio del solver...")
    status = solver.Solve(model)

    # 6. OUTPUT E VISUALIZZAZIONE
    if status == cp_model.OPTIMAL or status == cp_model.FEASIBLE:
        print(f'\nSoluzione Trovata!')
        print(f'Status: {solver.StatusName(status)}')
        print(f'Makespan Ottimo: {solver.ObjectiveValue()}')
        print('-' * 65)
        return int(solver.ObjectiveValue())
    else:
        print("Nessuna soluzione trovata nel tempo limite.")
        return None

