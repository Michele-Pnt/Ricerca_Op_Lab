    fig, ax = plt.subplots(figsize=(12, 6))
    bar_height = 0.6  # Spessore delle barre

    # Mappa le macchine a posizioni verticali (0, 1, 2...)
    machine_map = {m: i for i, m in enumerate(unique_machines)}

    max_time = 0

    # 4. Disegno delle barre
    for res in results:
        # Estrazione dati con gestione stringhe/numeri
        job = str(res['job'])
        machine = str(res['machine'])
