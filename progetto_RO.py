"""## Generazione Istanza del Problema

La funzione `generate_job_shop_instance` crea un'istanza semplificata di un problema di Job Shop Scheduling. Questo tipo di problema riguarda l'assegnazione di operazioni (tasks) di diversi "job" a diverse "macchine" in un ordine specifico, cercando di ottimizzare il makespan.

Ecco una scomposizione dei suoi parametri e del suo funzionamento:

### Parametri:
- `num_jobs`: Il numero di lavori (job) da generare nell'istanza. Ogni job avrà una serie di operazioni da eseguire.
- `num_machines`: Il numero di macchine disponibili. Ogni operazione di un job verrà assegnata a una di queste macchine.
- `proc_time_range`: Una tupla `(min_duration, max_duration)` che definisce l'intervallo minimo e massimo per la durata (tempo di elaborazione) di ciascuna operazione. La durata effettiva verrà scelta casualmente all'interno di questo intervallo.
- `seed`: Un valore opzionale per il seed del generatore di numeri casuali. Usarlo assicura che le istanze generate siano riproducibili (ovvero, eseguendo la funzione con lo stesso seed si otterranno sempre gli stessi risultati).

### Funzionamento:
1. **Inizializzazione**: Se viene fornito un `seed`, il generatore di numeri casuali di NumPy viene inizializzato con quel seed. Viene creata una lista vuota `instance` che conterrà tutte le operazioni generate.
2. **Definizione delle Macchine**: Viene creata una lista di `machines_list` da `0` a `num_machines - 1` per rappresentare le macchine disponibili.
3. **Generazione dei Job**: Per ogni job (`j` da `0` a `num_jobs - 1`):
   - Viene generata una `technological_sequence` casuale delle macchine. Questo significa che per ogni job, l'ordine in cui le macchine devono essere utilizzate è unico e casuale, ma ogni macchina viene visitata una sola volta per quel job (questa è una semplificazione comune per alcune varianti del Job Shop).
   - Per ogni macchina (`m_id`) nella sequenza tecnologica:
     - Viene generata una `duration` casuale per l'operazione sulla base di `proc_time_range`.
     - Un dizionario che rappresenta l'operazione viene aggiunto alla lista `instance`. Questo dizionario contiene:
       - `'job'`: L'ID del job (come stringa, partendo da '1').
       - `'op'`: L'ID dell'operazione all'interno del job (come stringa, partendo da '1').
       - `'machine'`: L'ID della macchina su cui l'operazione deve essere eseguita (come stringa).
       - `'duration'`: La durata dell'operazione.
4. **Ritorno**: La funzione restituisce la lista `instance` completa.
"""

def generate_job_shop_instance(num_jobs=4, num_machines=3, proc_time_range=(1, 10), seed=None):
    """
    Genera un'istanza piatta per un problema di Job Shop Scheduling.

    Returns:
        list: Una lista di dizionari, dove ogni dizionario è una singola operazione.
    """
    if seed is not None:
        np.random.seed(seed)

    instance = []
    machines_list = list(range(num_machines))

    for j in range(num_jobs):
        # Definiamo la sequenza tecnologica casuale per il job attuale
        technological_sequence = np.random.permutation(machines_list)

        for i, m_id in enumerate(technological_sequence):
            duration = np.random.randint(proc_time_range[0], proc_time_range[1] + 1)

            # Aggiungiamo direttamente l'operazione alla lista principale
            instance.append({
                "job": str(j + 1),
                "op": str(i + 1),
                "machine": str(m_id),
                "duration": duration
            })

    return instance #restituisce una lista di dizionari,vedi esempio sotto

