    current_schedule = [op.copy() for op in schedule]
    current_makespan = calculate_makespan(current_schedule)

    best_schedule = [op.copy() for op in current_schedule]
    best_makespan = current_makespan

    iteration = 0
    history = [(iteration, current_makespan)]
    temperature = initial_temp
    no_improve_count = 0

    while temperature > 1e-4 and no_improve_count < patience:
        improved_globally = False

        for _ in range(max_iterations):
            # 1. Analisi e scelta mossa casuale
            cp = find_critical_path(current_schedule)
            blocchi = identifica_blocchi(cp)
            mosse = []
            for b in blocchi: mosse.extend(genera_vicini_blocco(b))

            if not mosse: break
            mossa = random.choice(mosse)

            try:
                candidate_sched = applica_swap_e_shift(current_schedule, mossa)
                candidate_makespan = calculate_makespan(candidate_sched)

                # Calcolo del Delta (Differenza di costo/makespan)
                delta = candidate_makespan - current_makespan


                # CASO 1: DELTA < 0 (Miglioramento)
                if delta < 0:
                    # Accettiamo sempre la mossa
                    current_schedule = candidate_sched
                    current_makespan = candidate_makespan

                    # Verifichiamo se è il record assoluto (Best Ever)
                    if current_makespan < best_makespan:
                        best_makespan = current_makespan
                        best_schedule = copy.deepcopy(current_schedule)
                        improved_globally = True


                # CASO 2: DELTA >= 0 (Peggioramento o Stagnazione)
                else:
                    # Calcoliamo la probabilità di accettazione (Criterio di Metropolis)
                    probabilita = math.exp(-delta / temperature)

                    if random.random() < probabilita:
                        # Accettiamo la mossa peggiore per esplorare nuove aree
                        current_schedule = candidate_sched
                        current_makespan = candidate_makespan
                    # Altrimenti la mossa viene scartata e restiamo dove siamo


            except ValueError:
                continue

        # Gestione della pazienza e del raffreddamento
        if improved_globally:
            no_improve_count = 0
        else:
            no_improve_count += 1

        history.append((iteration, current_makespan))
        iteration += 1
        temperature *= cooling_rate

    # Stampa i risultati finali dell'algoritmo
