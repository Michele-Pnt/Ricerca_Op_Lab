# FUNZIONE PER ESEGUIRE IL FLUSSO PER OGNI ISTANZA
# ====================================================================

def run_instance_workflow(instance_name, raw_instance):
    """
    Esegue il flusso completo per un'istanza:
    - MOR: Greedy -> Local Search e Greedy -> Simulated Annealing
    - MWR: Greedy -> Local Search e Greedy -> Simulated Annealing
    - LPT: Greedy -> Local Search e Greedy -> Simulated Annealing
    
    Crea un grafico che confronta i makespan trovati rispetto all'ottimo.
    """
    
    print("\n" + "="*80)
    print(f"ISTANZA: {instance_name}".center(80))
    print("="*80)
    
    # Parsing istanza
    instance, n_j, n_m = parse_benchmark_instance(raw_instance)
    parsed_jobs = parse_instance(instance)

    # Dizionario per raccogliere i tempi (ms) con timer ad alta risoluzione
    timings_ms = {}
    
    # Calcolo ottimo con solver
    print("\nCalcolo dell'ottimo con Solver...")
    t0_ns = time.perf_counter_ns()
    optimum = solver(instance)
    timings_ms['Solver'] = (time.perf_counter_ns() - t0_ns) / 1_000_000
    print(f"Ottimo trovato: {optimum}\n")
    
    # Dizionario per raccogliere i risultati
    results = {
        'Ottimo (Solver)': optimum
    }
    
    greedy_functions = {
        'MOR': MOR_greedy,
        'MWR': MWR_greedy,
        'LPT': LPT_greedy
    }
    
    # Per ogni algoritmo greedy
    for greedy_name, greedy_func in greedy_functions.items():
        print(f"\n--- Processing {greedy_name} ---")
        
        # 1. Esegui greedy
        t0_ns = time.perf_counter_ns()
        greedy_schedule = greedy_func(instance, parsed_jobs)
        timings_ms[f'{greedy_name}\nGreedy'] = (time.perf_counter_ns() - t0_ns) / 1_000_000
        greedy_makespan = calculate_makespan(greedy_schedule)
        print(f"{greedy_name} Greedy Makespan: {greedy_makespan}")
        results[f'{greedy_name}\nGreedy'] = greedy_makespan
        plot_job_shop(greedy_schedule, title=f"Job Shop Scheduling ({greedy_name} Greedy) - Makespan: {greedy_makespan}")
        
        # 2. Local Search
        print(f"GANT CRITICAL PATH ")
        critical_path = find_critical_path(greedy_schedule)
        if critical_path:
            plot_job_shop(critical_path, title="Gantt Critical Path Soluzione Greedy MOR(iniziale)")
        else:
            print("Nessun cammino critico trovato.")
        print(f"Running Local Search on {greedy_name}...")

        t0_ns = time.perf_counter_ns()
        ls_best_schedule, ls_best_makespan, ls_history = local_search(
            greedy_schedule,
            max_iterations=100,
        )
        timings_ms[f'{greedy_name}\n+ LS'] = (time.perf_counter_ns() - t0_ns) / 1_000_000
        results[f'{greedy_name}\n+ LS'] = ls_best_makespan
        
        # 3. Simulated Annealing
        t0_ns = time.perf_counter_ns()
        sa_schedule, sa_makespan, sa_history = simulated_annealing(
            greedy_schedule,
            max_iterations=200,
            initial_temp=100.0,
            cooling_rate=0.95,
            patience=30
        )
        timings_ms[f'{greedy_name}\n+ SA'] = (time.perf_counter_ns() - t0_ns) / 1_000_000
        results[f'{greedy_name}\n+ SA'] = sa_makespan

        #4. Plot soluzione finale (Local Search)
        print(f"Makespan della soluzione {greedy_name} LC : {ls_best_makespan}")
        plot_job_shop(ls_best_schedule, title=f"Gantt Local Search - Makespan: {ls_best_makespan}")

        #5. Plot soluzione finale (Simulated Annealing)
        print(f"Makespan della soluzione {greedy_name} SA : {sa_makespan}")
        plot_job_shop(sa_schedule, title=f"Gantt Simulated Annealing - Makespan: {sa_makespan}")


        # 6. Plot storia del makespan (Local Search)
        print(f"PLOT EVOLUZIONE MAKESPAN {greedy_name} LC ")
        iterations, makespans = zip(*ls_history)
        fig_ls = plt.figure(figsize=(10, 6))
        plt.plot(iterations, makespans, marker='o', linestyle='-', linewidth=2, markersize=6)
        plt.xlabel('Iterazione', fontweight='bold')
        plt.ylabel('Makespan', fontweight='bold')
        plt.title('Evoluzione del Makespan durante Local Search', fontsize=14)
        plt.grid(True, alpha=0.5)
        plt.tight_layout()
        safe_show(fig_ls, title=f"{instance_name}_{greedy_name}_local_search")

        # 7. Plot storia del makespan (Simulated Annealing)
        print(f"PLOT EVOLUZIONE MAKESPAN {greedy_name} SA ")
        sa_iterations, sa_makespans = zip(*sa_history)
        fig_sa = plt.figure(figsize=(10, 6))
        plt.plot(sa_iterations, sa_makespans, marker='o', linestyle='-', linewidth=2, markersize=6)
        plt.xlabel('Iterazione', fontweight='bold')
        plt.ylabel('Makespan', fontweight='bold')
        plt.title('Evoluzione del Makespan durante Simulated Annealing', fontsize=14)
        plt.grid(True, alpha=0.5)
        plt.tight_layout()
        safe_show(fig_sa, title=f"{instance_name}_{greedy_name}_simulated_annealing")
    # ====================================================================
    # CREAZIONE GRAFICO PER L'ISTANZA
    # ====================================================================
    
    print(f"\nCreating comparison plot for {instance_name}...")
    
    # Preparazione dati per il grafico
    algorithms = list(results.keys())
    makespans = list(results.values())
    
    # Determinazione colori
    colors = []
