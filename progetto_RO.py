def parse_benchmark_instance(raw_text):
    """
    Legge una stringa in formato standard OR-Library (Lawrence/Fisher-Thompson)
    e la converte nel formato lista di dizionari per il tuo solver.

    Formato standard:
    - Prima riga: <num_jobs> <num_machines>
    - Righe successive: coppie (macchina, durata) per ogni operazione del job.
    """
    lines = raw_text.strip().split('\n')

    # Leggi intestazione
    # A volte ci sono spazi extra, quindi facciamo split e filter
    header = list(filter(None, lines[0].strip().split()))
    num_jobs = int(header[0])
    num_machines = int(header[1])

    instance_data = []

    # I job nel file iniziano dalla riga 1 (dopo l'header)
    for job_idx, line in enumerate(lines[1:]):
        if job_idx >= num_jobs: break # Sicurezza

        parts = list(filter(None, line.strip().split()))

        # Il formato standard è: machine_id duration machine_id duration ...
        # Attenzione: i dati grezzi sono coppie.

        op_counter = 1
        i = 0
        while i < len(parts):
            machine_id = int(parts[i])
            duration = int(parts[i+1])

            instance_data.append({
                'job': str(job_idx + 1),      # 1-based per coerenza col tuo codice
                'op': str(op_counter),
                'machine': str(machine_id),
                'duration': duration
            })

            op_counter += 1
            i += 2

            # esmepio variabile instance data:
            # [
            #   {'job': '1', 'op': '1', 'machine': '2', 'duration': 1},
            #   {'job': '1', 'op': '2', 'machine': '0', 'duration': 3},...
            # ]

    return instance_data, num_jobs, num_machines

