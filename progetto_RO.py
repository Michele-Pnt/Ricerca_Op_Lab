"""#MOR Greedy
Applichiamo la greedy chiamata Most Operations Remaining (MOR).
 La greedy mira a bilanciare i tempi complessivi dando priorità ai job con più operazioni da eseguire. In quanto l’alleato numero 1 della minimizzazione del makespan è il load balancing. essendo il makespan la durata del cammino critico -> facendo load balancing -> accorcio il cammino critico (perche le attività saranno piu distribuite) -> diminuisco il makespan. In caso di parità di scoring, cioè nel caso in cui due job hanno lo stesso numero di operazioni rimanenti viene applicato il cirterio di best MWR per decidere a chi dare priorità tra job dello spareggio.




"""

#----GREEDY----
def MOR_greedy(instance, jobs):
  # 1. Organizziamo i dati per accesso rapido
  job_ids = list(jobs.keys())

  # --- INIZIALIZZAZIONE STATO DEL SISTEMA ---

  # PUNTATORE: Indica a quale operazione è arrivato ogni job.
  # es: next_op_idx['1'] = 0 significa che il Job 1 deve ancora fare la prima op.
  next_op_idx = {j: 0 for j in job_ids}

  # TEMPO DISPONIBILITÀ JOB (Vincolo Tecnologico):
  # Un job non può iniziare l'operazione (i+1) prima che finisca l'operazione (i).
  # All'inizio tutti i job sono disponibili al tempo 0.
  job_available_time = {j: 0 for j in job_ids}

  # Identifichiamo le macchine uniche
  machines = set(op['machine'] for op in instance)

  # TEMPO DISPONIBILITÀ MACCHINA (Vincolo Risorsa):
  # Una macchina può processare solo un pezzo alla volta.
  # machine_available_time[M] indica quando la macchina M finirà il lavoro corrente.
  # inizialmente (non essendoci jobs in esecuzione) sono inizializzate a 0
  machine_available_time = {m: 0 for m in machines}

  results = [] # Lista dove salveremo lo schedule finale (Gantt)

  completed_jobs = 0
  total_jobs = len(job_ids)

  # --- CICLO DI SIMULAZIONE ---
  # Continua finché tutti i job non hanno finito tutte le loro operazioni
  while completed_jobs < total_jobs:

      # A. IDENTIFICAZIONE CANDIDATI
      # Chi è pronto a lavorare? Solo la PROSSIMA operazione di ogni job.
      candidates = []
      for j in job_ids:
          # Se il job j ha ancora operazioni da fare...
          if next_op_idx[j] < len(jobs[j]):
              # ...prendiamo l'operazione puntata dall'indice
              op = jobs[j][next_op_idx[j]]
              candidates.append(op)

      if not candidates:
          break # Sicurezza: nessun lavoro rimasto

      # B. CREAZIONE CODE PER MACCHINA
      # Raggruppiamo i candidati in base alla macchina che richiedono.
      # Es: Job 1 vuole M0, Job 2 vuole M1, Job 3 vuole M0 -> Coda su M0: [J1, J3]
      machine_queue = {m: [] for m in machines}
      for op in candidates:
          machine_queue[op['machine']].append(op)

      # C. RISOLUZIONE CONFLITTI (Core dell'algoritmo)
      for m in machines:
          queue = machine_queue[m]

          # Se nessuno vuole questa macchina in questo turno, passa alla prossima
          if not queue:
              continue

          # --- CALCOLO PRIORITÀ (MOR) ---
          scored_queue = []
          for op in queue:
              j_id = op['job']

              # Guardiamo quante operazioni mancano DOPO questa
              # Qui guardiamo il numero di op residuo totale inclusa l'operazione corrente
              # [next_op_idx[j_id]:]: cosi facendo prendo la lista delle operazioni di ogni job con
              # indice j_id fino alla fine A PARTIRE da next_op_idx[j_id]
