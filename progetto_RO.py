def calculate_makespan(results):
  """
  Calcola il Makespan di uno scheduling.

  Parametri:
  results: Lista di dizionari (output della funzione greedy).

  Ritorna:
  int/float: Il valore del Makespan (Cmax).
  """
  if not results:
    return 0

  # Per ogni operazione calcoliamo l'istante di fine: fine = inizio + durata
  # Il makespan è il massimo tra tutti questi istanti di fine.
  completion_times = [op['start'] + op['duration'] for op in results]
  makespan = max(completion_times)

  return makespan

"""# Individuazione del cammino critico"""

def find_critical_path(results):
    # Aggiungiamo il campo 'end' per facilitare i calcoli
    results_with_end = []
    for op_data in results:
        op_data_with_end = op_data.copy()
        op_data_with_end['end'] = op_data_with_end['start'] + op_data_with_end['duration']
        results_with_end.append(op_data_with_end)

    # Ordiniamo per tempo di fine decrescente
    sorted_res = sorted(results_with_end, key=lambda x: x['end'], reverse=True)
    c_max = sorted_res[0]['end']

    path = []
    current_op = sorted_res[0]
    path.append(current_op)

    while True:
        # Cerchiamo l'operazione che termina esattamente quando inizia current_op
        # e che sia o lo stesso job o sulla stessa macchina
        found = False
        for op in results_with_end:
            if op['end'] == current_op['start']:
                if op['job'] == current_op['job'] or op['machine'] == current_op['machine']:
                    current_op = op
                    path.append(op)
                    found = True
                    break
        if not found: break

    # Rimuovi la chiave 'end' aggiunta internamente per coerenza con il formato originale
    for op in path:
        if 'end' in op:
            del op['end']

    return list(reversed(path))

"""# Funzioni per individuare i blocchi e fare gli swap"""

def _op_id(op):
    """Genera un identificativo univoco (Job, Task) per l'operazione."""
    return (str(op['job']), int(op['op']))

def identifica_blocchi(cammino):
    """
    Raggruppa operazioni critiche consecutive sulla stessa macchina.
    Un blocco esiste solo se ci sono almeno 2 operazioni sulla stessa macchina.
    """
    blocchi = []
    if not cammino: return []
    blocco_attuale = [cammino[0]]
    for i in range(1, len(cammino)):
        if cammino[i]['machine'] == cammino[i-1]['machine']:
            blocco_attuale.append(cammino[i])
        else:
            if len(blocco_attuale) > 1:
                blocchi.append(list(blocco_attuale))
            blocco_attuale = [cammino[i]]
    if len(blocco_attuale) > 1:
        blocchi.append(blocco_attuale)
    return blocchi

def genera_vicini_blocco(blocco):
    """
    Genera le mosse basate sull'intorno N1 (Nowicki-Smutnicki).
    Ritorna una lista di tuple (id_op1, id_op2) da scambiare.
    """
    mosse = []
    if len(blocco) >= 2:
        # Mossa 1: Scambio le prime due operazioni del blocco
        mosse.append((_op_id(blocco[0]), _op_id(blocco[1])))

        # Mossa 2: Scambio le ultime due operazioni del blocco (se diverse dalle prime)
        if len(blocco) > 2:
            mosse.append((_op_id(blocco[-2]), _op_id(blocco[-1])))
    return mosse



def applica_swap_e_shift(schedule, mossa_swap):
    """
    Applica lo swap e ricalcola i tempi con Left-Shift.
    Usa l'algoritmo di Kahn per l'ordinamento topologico.
    """
    id1, id2 = mossa_swap
    new_schedule = [op.copy() for op in schedule]
    ops_dict = { _op_id(op): op for op in new_schedule }

    # 1. Determina le sequenze attuali sulle macchine
    machine_sequences = {}
    for op in sorted(new_schedule, key=lambda x: (x['machine'], x['start'])):
        m = op['machine']
        if m not in machine_sequences: machine_sequences[m] = []
        machine_sequences[m].append(_op_id(op))

    # 2. Applica lo swap nella sequenza della macchina specifica
    for m in machine_sequences:
        seq = machine_sequences[m]
        if id1 in seq and id2 in seq:
            idx1, idx2 = seq.index(id1), seq.index(id2)
            seq[idx1], seq[idx2] = seq[idx2], seq[idx1]
            break

    # 3. Costruzione del Grafo delle Precedenze
    adj = { oid: [] for oid in ops_dict }
    in_degree = { oid: 0 for oid in ops_dict }

    def add_edge(u, v):
        adj[u].append(v)
        in_degree[v] += 1

    # Vincoli dei Job (Successione task)
    job_groups = {}
    for oid in ops_dict: job_groups.setdefault(oid[0], []).append(oid)
    for j in job_groups:
        sorted_j = sorted(job_groups[j], key=lambda x: int(x[1]))
        for i in range(len(sorted_j)-1):
            add_edge(sorted_j[i], sorted_j[i+1])

    # Vincoli delle Macchine (Ordine post-swap)
    for m in machine_sequences:
        seq = machine_sequences[m]
        for i in range(len(seq)-1):
            add_edge(seq[i], seq[i+1])

    # 4. Ordinamento Topologico (Kahn)
    queue = [u for u in in_degree if in_degree[u] == 0]
    topo_order = []
    starts = { u: 0 for u in ops_dict }

    while queue:
        u = queue.pop(0)
        topo_order.append(u)
        finish_u = starts[u] + ops_dict[u]['duration']
        for v in adj[u]:
            starts[v] = max(starts[v], finish_u)
            in_degree[v] -= 1
            if in_degree[v] == 0:
                queue.append(v)

    # Se len(topo_order) < totale, abbiamo un ciclo (mossa non valida)
    if len(topo_order) < len(ops_dict):
        raise ValueError("Ciclo rilevato: mossa non ammissibile.")

    # 5. Aggiornamento dei risultati
    for oid, op in ops_dict.items():
        op['start'] = starts[oid]
        op['end'] = starts[oid] + op['duration']

    return list(ops_dict.values())

"""# Local search"""

def local_search(initial_schedule, max_iterations=100):
    current_schedule = initial_schedule
    current_makespan = calculate_makespan(current_schedule)

    history = [(0, current_makespan)]
    iteration=0

    for _ in range(max_iterations):
        found_improvement = False
        cp = find_critical_path(current_schedule)
        blocchi = identifica_blocchi(cp)


        best_neighbor = None
        best_neighbor_makespan = current_makespan


        for blocco in blocchi:

            candidati_vicini = genera_vicini_blocco(blocco)

            for v_blocco in candidati_vicini:

                try:
                    candidate_sched = applica_swap_e_shift(current_schedule, v_blocco)
                    m = calculate_makespan(candidate_sched)

                    if m < best_neighbor_makespan:
                        best_neighbor_makespan = m
                        best_neighbor = candidate_sched
                        found_improvement = True

                except ValueError:
                    continue


        if found_improvement:
            current_schedule = best_neighbor
            current_makespan = best_neighbor_makespan
            history.append((iteration, best_neighbor_makespan))
        else:
            break # Ottimo locale raggiunto
        iteration += 1

    print(f"\n=== Local Search2 Terminata ===")
    print(f"Iterazioni totali: {iteration}")
    print(f"Makespan iniziale: {calculate_makespan(initial_schedule)}")
    print(f"Makespan finale: {current_makespan}")
    print(f"Miglioramento: {calculate_makespan(initial_schedule) - current_makespan}")

    return current_schedule,current_makespan,history

"""# Simulated Annealing"""

def simulated_annealing(schedule, max_iterations=10, initial_temp=100.0, cooling_rate=0.95, patience=50):
    """
    Simulated Annealing con swap di blocchi e left-shift.

    Args:
        schedule: soluzione iniziale (lista di operazioni)
        max_iterations: numero massimo di iterazioni per temperatura
        initial_temp: temperatura iniziale
        cooling_rate: fattore di raffreddamento (0 < cooling_rate < 1)
        patience: numero massimo di non miglioramenti consecutivi

    Returns:
        best_schedule: migliore soluzione trovata
        best_makespan: makespan della migliore soluzione
        history: storico di makespan e iterazioni
    """

