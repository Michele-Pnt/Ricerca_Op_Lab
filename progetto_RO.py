    if not results:
        print("Nessun dato da visualizzare.")
        return

    # 1. Identifica macchine e job unici
    # Usiamo sorted per avere un ordine consistente negli assi e nei colori
    unique_jobs = sorted(list({str(res['job']) for res in results}))
    unique_machines = sorted(list({str(res['machine']) for res in results}))

    # 2. Setup Colormap (tab20 offre 20 colori distinti)
    colors = plt.cm.tab20(range(len(unique_jobs)))
    job_colors = {job: colors[i % 20] for i, job in enumerate(unique_jobs)}

