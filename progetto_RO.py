    print(f"Iterazioni totali: {iteration}")
    print(f"Makespan iniziale: {calculate_makespan(schedule)}")
    print(f"Makespan migliore: {best_makespan}")
    print(f"Miglioramento: {calculate_makespan(schedule) - best_makespan}")

    return best_schedule, best_makespan, history

